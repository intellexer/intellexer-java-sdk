package by.effectivesoft.intellexer.exception;

public class IntellexerException extends Exception {
    public IntellexerException() {
        super();
    }

    public IntellexerException(String message) {
        super(message);
    }

    public IntellexerException(String message, Throwable cause) {
        super(message, cause);
    }

    public IntellexerException(Throwable cause) {
        super(cause);
    }
}
